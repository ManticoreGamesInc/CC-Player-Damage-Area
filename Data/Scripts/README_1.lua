This component can be used to apply damage over time to the player when they are in an area. The area can
be resized and moved easily, and multiple areas can be created.

-----------------------------------------------------------------------------------------------------------------

==========
How to use
==========

Drag the template into the Hierarchy and place it where you want the damage area to be.

This component works well with the Player Healthbar component so the player can see
how much health they have.

- DamagePerSecond

The amount of damage the player will receive per second.

